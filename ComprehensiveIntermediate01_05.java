class ComprehensiveIntermediate01_05 {
    public static void main(String[] args) {
        for (int i = 1; i <= 9; i++) { 
                // スペースを出力（右寄せ用）
                for (int j = 1; j <= 9; j++) {
                  System.out.print(i * j +"\t");
                }
                System.out.println();
            }
    }
}